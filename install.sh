#!/bin/sh
set -eu

DIST_REPO_OWNER="${CLAUDE_FUSION_DIST_OWNER:-Caffeine-Ops}"
DIST_REPO_NAME="${CLAUDE_FUSION_DIST_REPO:-fusion-claude-code-releases}"
DEFAULT_VERSION="snapshot-9fe2c2e"
INSTALL_REF="${CLAUDE_FUSION_VERSION:-$DEFAULT_VERSION}"
INSTALL_HOME="${CLAUDE_FUSION_HOME:-$HOME/.claude-fusion}"
BIN_DIR="${CLAUDE_FUSION_BIN_DIR:-$HOME/.local/bin}"
APP_ROOT="$INSTALL_HOME/app"
APP_DIR="$APP_ROOT/$INSTALL_REF"
CURRENT_LINK="$INSTALL_HOME/current"
CONFIG_FILE="$INSTALL_HOME/config.env"
LAUNCHER_PATH="$BIN_DIR/claude-fusion"
ARCHIVE_URL="${CLAUDE_FUSION_ARCHIVE_URL:-https://github.com/$DIST_REPO_OWNER/$DIST_REPO_NAME/releases/download/$INSTALL_REF/fusion-claude-code-$INSTALL_REF.tar.gz}"
DEFAULT_BASE_URL="${CLAUDE_FUSION_DEFAULT_BASE_URL:-https://api.anthropic.com}"
DEFAULT_MODEL="${CLAUDE_FUSION_DEFAULT_MODEL:-claude-sonnet-4-6}"
TTY_DEVICE="/dev/tty"

say() {
  printf '%s\n' "$*"
}

fail() {
  printf 'Error: %s\n' "$*" >&2
  exit 1
}

require_command() {
  if ! command -v "$1" >/dev/null 2>&1; then
    fail "$1 is required"
  fi
}

ensure_supported_platform() {
  os="$(uname -s 2>/dev/null || printf unknown)"
  case "$os" in
    Darwin|Linux) ;;
    *) fail "This installer currently supports macOS and Linux only" ;;
  esac
}

ensure_bun() {
  if command -v bun >/dev/null 2>&1; then
    return
  fi

  say "Bun not found. Installing Bun..."

  if ! command -v bash >/dev/null 2>&1; then
    fail "bash is required to install Bun automatically. Install Bun from https://bun.sh and rerun this installer"
  fi

  if ! command -v unzip >/dev/null 2>&1; then
    fail "unzip is required to install Bun. Please install unzip first, then rerun this installer"
  fi

  bash -c "$(curl -fsSL https://bun.sh/install)"
  export PATH="$HOME/.bun/bin:$PATH"

  if ! command -v bun >/dev/null 2>&1; then
    fail "Bun installation finished but bun is still not on PATH. Reopen the terminal or add ~/.bun/bin to PATH and rerun this installer"
  fi
}

prompt_value() {
  prompt="$1"
  default_value="${2-}"

  if [ -n "$default_value" ]; then
    printf '%s [%s]: ' "$prompt" "$default_value" >"$TTY_DEVICE"
  else
    printf '%s: ' "$prompt" >"$TTY_DEVICE"
  fi

  read -r value <"$TTY_DEVICE"
  if [ -z "$value" ]; then
    value="$default_value"
  fi
  printf '%s' "$value"
}

prompt_secret() {
  prompt="$1"
  printf '%s: ' "$prompt" >"$TTY_DEVICE"
  stty -echo <"$TTY_DEVICE" 2>/dev/null || true
  read -r value <"$TTY_DEVICE"
  stty echo <"$TTY_DEVICE" 2>/dev/null || true
  printf '\n' >"$TTY_DEVICE"
  printf '%s' "$value"
}

prompt_auth_mode() {
  printf 'Authentication mode [1=ANTHROPIC_AUTH_TOKEN, 2=ANTHROPIC_API_KEY] [1]: ' >"$TTY_DEVICE"
  read -r auth_mode <"$TTY_DEVICE"
  if [ -z "$auth_mode" ]; then
    auth_mode="1"
  fi
  case "$auth_mode" in
    1|2) printf '%s' "$auth_mode" ;;
    *) fail "Invalid authentication mode: $auth_mode" ;;
  esac
}

write_config() {
  mkdir -p "$INSTALL_HOME"

  if [ -f "$CONFIG_FILE" ]; then
    reuse="$(prompt_value "Existing config found. Reuse it? (y/n)" "y")"
    case "$reuse" in
      y|Y|yes|YES)
        say "Reusing existing config at $CONFIG_FILE"
        chmod 600 "$CONFIG_FILE" 2>/dev/null || true
        return
        ;;
    esac
  fi

  auth_mode="$(prompt_auth_mode)"
  base_url="$(prompt_value "ANTHROPIC_BASE_URL" "$DEFAULT_BASE_URL")"
  model="$(prompt_value "ANTHROPIC_MODEL" "$DEFAULT_MODEL")"

  if [ "$auth_mode" = "1" ]; then
    secret_name="ANTHROPIC_AUTH_TOKEN"
  else
    secret_name="ANTHROPIC_API_KEY"
  fi

  secret_value="$(prompt_secret "$secret_name")"
  if [ -z "$secret_value" ]; then
    fail "$secret_name cannot be empty"
  fi

  umask 077
  cat > "$CONFIG_FILE" <<EOF
# Generated by claude-fusion install script.
# Edit this file to update your endpoint or model.
$secret_name=$secret_value
ANTHROPIC_BASE_URL=$base_url
ANTHROPIC_MODEL=$model
ANTHROPIC_DEFAULT_SONNET_MODEL=$model
ANTHROPIC_DEFAULT_HAIKU_MODEL=$model
ANTHROPIC_DEFAULT_OPUS_MODEL=$model
DISABLE_TELEMETRY=1
CLAUDE_CODE_DISABLE_NONESSENTIAL_TRAFFIC=1
EOF
  chmod 600 "$CONFIG_FILE"
}

install_source_tree() {
  mkdir -p "$APP_ROOT"

  if [ ! -d "$APP_DIR" ]; then
    tmp_dir="$(mktemp -d "${TMPDIR:-/tmp}/claude-fusion.XXXXXX")"
    archive_path="$tmp_dir/source.tar.gz"

    say "Downloading source archive for $INSTALL_REF..."
    if ! curl -fsSL "$ARCHIVE_URL" -o "$archive_path"; then
      fail "Failed to download source archive from $ARCHIVE_URL. If this repository is private, publish the archive URL or rerun with CLAUDE_FUSION_ARCHIVE_URL set to a reachable tar.gz"
    fi

    say "Extracting source archive..."
    tar -xzf "$archive_path" -C "$tmp_dir"

    extracted_dir="$(cd "$tmp_dir" && ls -1 | grep -v '^source.tar.gz$' | head -n 1)"
    [ -n "$extracted_dir" ] || fail "Failed to locate extracted source directory"

    mv "$tmp_dir/$extracted_dir" "$APP_DIR"
    rm -rf "$tmp_dir"
  else
    say "Source for $INSTALL_REF already exists, reusing it"
  fi

  say "Installing dependencies with Bun..."
  (
    cd "$APP_DIR"
    bun install
  )

  ln -sfn "$APP_DIR" "$CURRENT_LINK"
}

write_launcher() {
  mkdir -p "$BIN_DIR"

  cat > "$LAUNCHER_PATH" <<'EOF'
#!/bin/sh
set -eu

INSTALL_HOME="${CLAUDE_FUSION_HOME:-$HOME/.claude-fusion}"
APP_DIR="$INSTALL_HOME/current"
CONFIG_FILE="$INSTALL_HOME/config.env"

if command -v bun >/dev/null 2>&1; then
  BUN_CMD="bun"
elif [ -x "$HOME/.bun/bin/bun" ]; then
  BUN_CMD="$HOME/.bun/bin/bun"
else
  printf 'Error: bun is not installed or not on PATH\n' >&2
  exit 1
fi

if [ ! -d "$APP_DIR" ]; then
  printf 'Error: installed app directory not found at %s\n' "$APP_DIR" >&2
  exit 1
fi

if [ ! -f "$CONFIG_FILE" ]; then
  printf 'Error: config file not found at %s\n' "$CONFIG_FILE" >&2
  exit 1
fi

export CLAUDE_CONFIG_DIR="$INSTALL_HOME"
cd "$APP_DIR"

if [ "${CLAUDE_CODE_FORCE_RECOVERY_CLI:-0}" = "1" ]; then
  exec "$BUN_CMD" --env-file="$CONFIG_FILE" ./src/localRecoveryCli.ts "$@"
fi

exec "$BUN_CMD" --env-file="$CONFIG_FILE" ./src/entrypoints/cli.tsx "$@"
EOF

  chmod 755 "$LAUNCHER_PATH"
}

print_path_note() {
  case ":$PATH:" in
    *":$BIN_DIR:"*) ;;
    *)
      say ""
      say "Add $BIN_DIR to your PATH if claude-fusion is not found in a new shell:"
      say "  export PATH=\"$BIN_DIR:\$PATH\""
      ;;
  esac
}

run_smoke_test() {
  say "Running smoke test..."
  "$LAUNCHER_PATH" --help >/dev/null
}

main() {
  ensure_supported_platform
  require_command curl
  require_command tar

  if [ ! -r "$TTY_DEVICE" ] || [ ! -w "$TTY_DEVICE" ]; then
    fail "Interactive install requires a TTY. Run this script from a terminal"
  fi

  ensure_bun
  install_source_tree
  write_config
  write_launcher
  run_smoke_test

  say ""
  say "claude-fusion installed successfully."
  say "Launcher: $LAUNCHER_PATH"
  say "Config:   $CONFIG_FILE"
  print_path_note
  say ""
  say "Try: claude-fusion --help"
}

main "$@"
